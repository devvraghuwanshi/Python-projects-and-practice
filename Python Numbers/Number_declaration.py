# there are three numeric types in python.
# 1. int - integer,is a whole number,positive or negative, without decimal,has unlimited length.
# 2. float - floating point number,positive or negative , containing one or more decimal.
# 3. complex - written with j as imaginary part.

a = 1
b = 10.20
c = 2j

# type(variable) - gives datatype of any object
print(type(a))
print(type(b))
print(type(c))

