# Escape characters are special characters used in strings
# They start with a backslash (\)

# \n -> New line
print("Hello\nWorld")  
# Output will print World on the next line

# \t -> Tab space
print("Hello\tWorld")  
# Adds a tab space between Hello and World

# \\ -> Backslash
print("This is a backslash: \\")  
# Prints a single backslash

# \" -> Double quote inside string
print("He said \"Hello\" to me")  
# Allows using double quotes inside a string

# \' -> Single quote inside string
print('It\'s a sunny day')  
# Allows using single quote inside string

# \r -> Carriage return (moves cursor to start of line)
print("Hello\rWorld")  
# World overwrites Hello

# \b -> Backspace
print("Helloo\b")  
# Removes the last character

# \f -> Form feed
print("Hello\fWorld")

# \v -> Vertical tab
print("Hello\vWorld")