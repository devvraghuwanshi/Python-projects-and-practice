str = "  this is an example of modification of string.  "

# upper() - returns the string in upper case.
print(str.upper())

# lower() - returns the string in lower case.
print(str.lower())

# strip() - removes whitespace from beginning or the end.
print(str.strip())

# replace() - replace a string with another string.
# syntx - str_name.replace("existing" , "new")
print(str.replace("this" , "new"))

# split() - returns a list where the text between the specified seperator becomes the list item.
print(str.split(" ")) 