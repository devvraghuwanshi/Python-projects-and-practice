# returns a range of characters by using slicing index.
# Syntax = str_name[start_index : End_index(exclusive)].

str = "this is example of string slicing"
print(str[0:4])
print(len(str))

# slice from start
print(str[:4])

# slice to end
print(str[0:])

# Negative index - to start slice from the end of the string.
print(str[-33:-26])