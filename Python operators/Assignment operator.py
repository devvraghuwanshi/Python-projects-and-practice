#Assignment operator - used to assign values to variable.

x = 10   #basic assignment
x += 5   #Add and assign
print(x)

y = 5
y -= 5   #Subtract and assign
print(y)

z = 10
z *= 2   #Multiply and assign
print(z)

a = 10
a /= 10  # divide and assign
print(a)

b = 10
b %= 10  #Modulus and assign
print(b)

c = 10
c //= 2  #Floor divide and assign
print(c)

d = 20
d **= 2  #Power and assign
print(d)