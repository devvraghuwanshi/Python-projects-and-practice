#Operators - symbols used to perform operations on values and variables that holds those values.

#Arithmetic operator - used with numeric values.

a = 10
b = 10
print(a + b)      #addition
print(a - b)      #substraction  
print(a * b)      #multiplication
print(a % b)      #modulus - remainder
print(a ** b)     # exponentiation
print(a / b)      # Division - returns a float
print(a // b)     # Floor division - returns an integer