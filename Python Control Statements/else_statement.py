# SYNTAX(Full if-elif-else Structure) :
# if condition1:
#     # code
# elif condition2:
#     # code
# elif condition3:
#     # code
# else:
#     # runs if all conditions are False


# WORKING:The else statement is executed when the if condition (and any elif conditions) evaluate to False.

# EXAMPLE:
a = 200
b = 33
if b > a:
  print("b is greater than a")
elif a == b:
  print("a and b are equal")
else:
  print("a is greater than b")