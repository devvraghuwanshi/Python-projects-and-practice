student_marks = {
    "Dev" : 100,
    "Raj" : 60,
    "Sagar" : 100
}

# Printing key names - 
for x in student_marks:
    print(x)

for y in student_marks.keys():
    print(y)

# Printing values - 
for a in student_marks:
    print(student_marks[a])

for b in student_marks.values():
    print(b)

# Printing items - 
for n,m in student_marks.items():
    print(n,m)